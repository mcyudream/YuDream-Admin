const vue = shared('vue')

function shared(name: string) {
  const modules = (globalThis as any).__YUDREAM_PLUGIN_SHARED__
  const module = modules?.[name]
  if (!module) {
    throw new Error(`YuDream plugin shared runtime is missing: ${name}`)
  }
  return module
}

